/**
 * Used types for transform package into array buffer
 */
export enum Type {
  /**
   * Byte data type
   */
  Byte = 1,

  /**
   * Unsigned byte data type
   */
  UByte = 2,

  /**
   * Signed short int data type
   */
  ShortInt = 3,

  /**
   * Unsigned short int data type
   */
  UShortInt = 4,

  /**
   * Signed int data type
   */
  Int = 5,

  /**
   * Unsigned int data type
   */
  UInt = 6,

  /**
   * Float data type
   */
  Float = 7,

  /**
   * Double data type
   */
  Double = 8,
}
