
/**
 * Server config
 *
 * @export
 * @interface IServerConfig
 */
export interface IServerConfig {
  /**
   * Websocket host
   *
   * @type {string}
   * @memberof IServerConfig
   */
  host?: string;

  /**
   * Websocket port
   *
   * @type {number}
   * @memberof IServerConfig
   */
  port?: number;
}
