/**
 * Used sizes for transform package into array buffer
 */
export enum Size {
  /**
   * Empty data type size
   */
  Empty = 0,

  /**
   * Byte data type size
   */
  Byte = 1,

  /**
   * Signed short int data type size
   */
  ShortInt = 2,

  /**
   * Unsigned short int data type size
   */
  UShortInt = 2,

  /**
   * Signed int data type size
   */
  Int = 4,

  /**
   * Unsigned int data type size
   */
  UInt = 4,

  /**
   * Float data type size
   */
  Float = 4,

  /**
   * Double data type size
   */
  Double = 8,
}
